package lessons2022;

public class Java2_Variables08192022 {
    public static void main(String[] args) {
        //Decleration of variables
        string name = "Kristian";     //Text or String
        int mynum = 5;                //Integer(Whole number)
        float myFloatNum = 5.99f;     //Floating point number(decimal)
        char myLetter = 'G';          //Character, you can use numbers from ICII table to define
        boolean myBool = true;        //Boolean
        byte smallNumber = 3;         //Stores whole numbers from -128 to 127
        short shortNumber = 4;        //Whole numbers from -32,768 to 32,767
        long longNumber = 123456789;  //Whole numbers from -9,223,372,836,854,775,808 to 9,223,372,836,854,775,808
        double doubleNumber = 12.34;  //Fractional numbers, storing 15 decimal digits
        //Rules for Variables
        /*
        you can't give a reserved name for a variable like class etc...
        it should not start from a number
        Always start with lowercase
        prims are byte, short, long, boolean, int, float, double, char
        float ends with f
        double ends with d but doesn't have to
        long ends with l but doesn't have to
        char has only one character within single quote
        boolean is only true and false
         */

        int myNum = 9;
        float myFloatNum = 8.99f;
        char myLetter = 'k';
        boolean myBool = false;
        string myText = "Hello World";
    }
}
