public class ProjectSumTwoValues11172022 {
    public static int sumDoubleOrPower(int a, int b){
        if(a==b){
            return a+b;
        }
        else{
            return a+b;
        }
    }

    public static void main(String[] args) {
        sumDoubleOrPower(3,3);
    }
}
